// problem: https://leetcode.cn/problems/max-consecutive-ones-iii/?envType=problem-list-v2&envId=3rBpGyas
struct Solution;

impl Solution {
    pub fn longest_ones(nums: Vec<i32>, k: i32) -> i32 {
        let mut res = 0;
        let mut zeroCnts = vec![];
        let mut oneCnts = vec![];
        let mut cnt = 0;
        let n = nums.len();
        let mut last = 0;
        for i in 0..n {
            if nums[i] != last {
                if nums[i] == 1 {
                    zeroCnts.push(cnt);
                } else {
                    oneCnts.push(cnt);
                }
                cnt = 1;
            } else {
                cnt += 1;
            }
            last = nums[i];
        }
        if nums[n - 1] == 1 {
            oneCnts.push(cnt);
        } else {
            zeroCnts.push(cnt);
        }
        if k >= zeroCnts.iter().sum() {
            return nums.len() as i32;
        }

        let mut zStartPos = 0;
        let mut zEndPos = 0;
        let mut zSum = 0;
        let mut oSum = 0;
        while zEndPos < usize::min(zeroCnts.len(), oneCnts.len()) {
            zSum += zeroCnts[zEndPos];
            oSum += oneCnts[zEndPos];
            while zSum > k {
                zSum -= zeroCnts[zStartPos];
                if zStartPos > 0 {
                    oSum -= oneCnts[zStartPos - 1];
                }
                zStartPos += 1;
            }
            res = i32::max(res, k + oSum);
            zEndPos += 1;
        }

        res = i32::max(res, k + oSum);
        res
    }
}

