// problem: https://leetcode.cn/problems/bulb-switcher-ii/description/
struct Solution;

impl Solution {
    pub fn flip_lights(n: i32, presses: i32) -> i32 {
        if presses == 0 {
            return 1;
        }
        match n {
            1 => 2,
            2 => match presses {
                1 => 3,
                _ => 4,
            },
            _ => match presses {
                1 => 4,
                2 => 7,
                _ => 8,
            },
        }
    }
}

fn main() {
    println!("{:?}", Solution::flip_lights(1, 1));
}

