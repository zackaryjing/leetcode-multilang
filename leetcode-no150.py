class Solution(object):
    def evalRPN(self, tokens):
        """
        :type tokens: List[str]
        :rtype: int
        """
        process = []
        for i in tokens:
            if i in "+*":
                process.append(str(eval(process.pop()+i+process.pop())))
            elif i in "/-":
                a = int(process.pop())
                b = int(process.pop())
                if i == '/':
                    process.append(str(int(b/a)))
                else:
                    process.append(str(b-a))
            else:
                process.append(i)
        return int(process[-1])

