class Solution {
public:
    vector<double> sampleStats(vector<int> &count) {
        vector<double> res(5);
        bool fmn = false;
        int total = 0;
        int cnt_mx = 0;
        int half_total = 0;
        int last_pos = 0;
        for (int i = 0; i < count.size(); ++i) {
            if (not fmn and count[i] > 0) {
                res[0] = i;
                fmn = true;
            }
            if (count[i] > 0) {
                res[1] = i;
            }

            res[2] += static_cast<double>(count[i]) * i;
            total += count[i];
            while (half_total <= total >> 1) {
                half_total += count[last_pos++];
            }

            if (count[i] > cnt_mx) {
                res[4] = i;
                cnt_mx = count[i];
            }
        }
        if (total & 1) {
            res[3] = last_pos - 1;
        } else {
            if ((half_total - count[--last_pos]) < total >> 1) {
                res[3] = last_pos;
            } else {
                res[3] = last_pos;
                last_pos -= 1;
                while (count[last_pos] == 0)
                    last_pos--;
                res[3] = (res[3] + last_pos) / 2.0;
            }
        }
        res[2] /= total;
        return res;
    }
};

